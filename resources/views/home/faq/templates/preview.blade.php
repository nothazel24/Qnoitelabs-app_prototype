@push('scripts')
    <script>
        document.querySelectorAll(".toggle-btn").forEach(function(btn) {
            btn.addEventListener("click", function() {
                const card = btn.closest(".inner");
                const extraFeatures = card.querySelector(".extra-features");

                if (extraFeatures.style.maxHeight && extraFeatures.style.maxHeight !== "0px") {
                    extraFeatures.style.maxHeight = null;
                    btn.classList.remove("active");
                } else {
                    document.querySelectorAll(".extra-features").forEach(el => {
                        el.style.maxHeight = null;
                        el.closest(".inner").querySelector(".toggle-btn").classList.remove(
                            "active");
                    });

                    extraFeatures.style.maxHeight = extraFeatures.scrollHeight + "px";
                    btn.classList.add("active");
                }
            });
        });
    </script>
@endpush
